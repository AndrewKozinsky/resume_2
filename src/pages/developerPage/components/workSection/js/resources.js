export function createImages(type, page) {
    const imagesArr = getImagesSrc(type, page)

    return imagesArr.map(srcsObj => {
        return createPicture(srcsObj, type)
    })
}

function createPicture(srcsObj, type) {

    const sourses = []

    let imageSrcSet

    if(srcsObj.img_1) {
        let srcSet = srcsObj.img_1
        if(srcsObj.img_1_2x) srcSet += `, ${srcsObj.img_1_2x} 2x`

        imageSrcSet = srcSet

        sourses.push(
            <source srcSet={srcSet} key='1' />
        )
    }

    if(srcsObj.img_2) {
        let srcSet = srcsObj.img_2
        if(srcsObj.img_2_2x) srcSet += `, ${srcsObj.img_2_2x} 2x`

        imageSrcSet = srcSet

        sourses.push( <source srcSet={srcSet} key='2' /> )
    }

    return <picture key={srcsObj.img_1}>
        {sourses}
        <img src={srcsObj.img_1} srcSet={imageSrcSet} alt={type}/>
    </picture>
}

